package com.brimmatech.dao;

public class AssetDetailsClass {
    private String serialnumber;
    private String modelnumber;
    private String assettype;
    private String assetname;
    private String status;
    private String reason;
    private String emailid;
    private String name;
    private String assetreturntype;
    private String allocatedon;

    public String getSerialnumber() {
        return serialnumber;
    }

    public void setSerialnumber(String serialnumber) {
        this.serialnumber = serialnumber;
    }

    public String getModelnumber() {
        return modelnumber;
    }

    public void setModelnumber(String modelnumber) {
        this.modelnumber = modelnumber;
    }

    public String getAssettype() {
        return assettype;
    }

    public void setAssettype(String assettype) {
        this.assettype = assettype;
    }

    public String getAssetname() {
        return assetname;
    }

    public void setAssetname(String assetname) {
        this.assetname = assetname;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getEmailid() {
        return emailid;
    }

    public void setEmailid(String emailid) {
        this.emailid = emailid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAssetreturntype() {
        return assetreturntype;
    }

    public void setAssetreturntype(String assetreturntype) {
        this.assetreturntype = assetreturntype;
    }

    public String getAllocatedon() {
        return allocatedon;
    }

    public void setAllocatedon(String allocatedon) {
        this.allocatedon = allocatedon;
    }


}
